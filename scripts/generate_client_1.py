def main():
    parser = argparse.ArgumentParser(description="Generate unified client from configuration")
    parser.add_argument("--config", required=True, help="Configuration directory")
    parser.add_argument("--output", required=True, help="Output directory")
    parser.add_argument("--language", choices=["typescript", "python"], default="typescript")
    
    args = parser.parse_args()
    
    config_dir = Path(args.config)
    output_dir = Path(args.output)
    
    if not config_dir.exists():
        print(f"❌ Config directory not found: {config_dir}")
        sys.exit(1)
    
    output_dir.mkdir(parents=True, exist_ok=True)
    
    print(f"🔧 Generating {args.language} client from {config_dir}")
    
    config = load_config(config_dir)
    
    if args.language == "typescript":
        generate_typescript_types(config, output_dir)
        generate_typescript_client(config, output_dir)
        generate_typescript_cli(config, output_dir)
    else:
        generate_python_client(config, output_dir)
    
    print(f"\n✅ Client generated successfully in {output_dir}")


if __name__ == "__main__":
    main()
