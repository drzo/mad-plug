#!/usr/bin/env python3
"""
Generate unified client code from configuration files.

Usage:
    python generate_client.py --config <config-dir> --output <output-dir> --language <typescript|python>

Example:
    python generate_client.py --config ./config --output ./src --language typescript
"""

import argparse
