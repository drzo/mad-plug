BANNER = """
+==============================================================+
|  Plan 9 File Server Cluster Configurator                      |
|  plan9-devcontainer(plan9-cognitive-devkernel[file-server])    |
+==============================================================+
"""


def main():
    parser = argparse.ArgumentParser(description="Plan 9 File Server Cluster Configurator")
    sub = parser.add_subparsers(dest="command")

    # init
    p_init = sub.add_parser("init", help="Initialize cluster configuration")
    p_init.add_argument("project_path", help="Project directory")
    p_init.add_argument("--cpus", type=int, default=2, help="Number of CPU servers (default: 2)")
    p_init.add_argument("--domain", default="coglab", help="Auth domain name (default: coglab)")
    p_init.add_argument("--subnet", default="192.168.1.0", help="Subnet (default: 192.168.1.0)")
    p_init.add_argument("--filesystem", default="hjfs", choices=["hjfs", "cwfs"],
                        help="File system type (default: hjfs)")
    p_init.add_argument("--no-cognitive", action="store_true", help="Disable cognitive namespace")

    # validate
    p_val = sub.add_parser("validate", help="Validate cluster configuration")
    p_val.add_argument("project_path", help="Project directory")

    # status
    sub.add_parser("status", help="Show cluster status")

    args = parser.parse_args()

    if args.command == "init":
        print(BANNER)
        print(f"  Initializing Plan 9 cluster at: {args.project_path}")
        print()
        cfg = ClusterConfig(
            domain=args.domain,
            subnet=args.subnet,
            num_cpus=args.cpus,
            filesystem=args.filesystem,
            cognitive_ns=not args.no_cognitive,
        )
        init_cluster(args.project_path, cfg)

    elif args.command == "validate":
        print(BANNER)
        print(f"  Validating cluster at: {args.project_path}")
        print()
        results = validate_cluster(args.project_path)
        for r in results:
            icon = "+" if r["satisfied"] else "X"
            print(f"  {icon} {r['name']}: {r['description']}")
            if r["evidence"]:
                print(f"       Evidence: {r['evidence']}")
        satisfied = sum(1 for r in results if r["satisfied"])
        total = len(results)
        print()
        if satisfied == total:
            print(f"  + All {total} cluster promises satisfied.")
        else:
            print(f"  X {satisfied}/{total} cluster promises satisfied.")

    elif args.command == "status":
        print(BANNER)
        print("  File Server Cluster Promises:")
        print()
        for name, desc in CLUSTER_PROMISES:
            print(f"    {name:20s} — {desc}")

    else:
        parser.print_help()


if __name__ == "__main__":
    main()
