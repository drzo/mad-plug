#!/usr/bin/env python3
"""
Plan 9 File Server Cluster Configurator

Generates all configuration files for a Plan 9 file server + N CPU server cluster
with cognitive namespace exports. Integrates with plan9-devcontainer and
plan9-cognitive-devkernel.

Composition:
  plan9-devcontainer(plan9-cognitive-devkernel[{{file-server}}])

Usage:
  python configure_cluster.py init <project_path> [--cpus N] [--domain NAME] [--subnet CIDR]
  python configure_cluster.py validate <project_path>
  python configure_cluster.py status
"""

import argparse
import json
import os
import sys
import textwrap
from dataclasses import dataclass, field, asdict
from pathlib import Path
from typing import Optional


# =============================================================================
# Cluster Model
# =============================================================================

@dataclass
class NodeConfig:
    """A single node in the Plan 9 cluster."""
    sysname: str
    role: str  # "file-server", "cpu-server", "auth-server"
    ip: str
    ether: str = "xx:xx:xx:xx:xx:xx"
    auth: str = ""
    authdom: str = ""
    fs: str = ""
    cpu: str = ""
    ports: list = field(default_factory=list)

    @property
    def dom(self) -> str:
        return f"{self.sysname}.{self.authdom}"


@dataclass
class ClusterConfig:
    """Full Plan 9 cluster configuration."""
    domain: str = "coglab"
    subnet: str = "192.168.1.0"
    netmask: str = "255.255.255.0"
    gateway: str = "192.168.1.1"
    dns: str = "192.168.1.1"
    fs_ip: str = "192.168.1.10"
    cpu_base_ip: int = 11
    num_cpus: int = 2
    filesystem: str = "hjfs"  # hjfs (9front) or cwfs (classic)
    cognitive_ns: bool = True
    cognitive_port: int = 5640

    @property
    def fs_node(self) -> NodeConfig:
        return NodeConfig(
            sysname="fs",
            role="file-server",
            ip=self.fs_ip,
            auth=f"fs.{self.domain}",
            authdom=self.domain,
            ports=[564, 567, 17010, 17013],
        )

    @property
    def cpu_nodes(self) -> list[NodeConfig]:
        nodes = []
        for i in range(self.num_cpus):
            ip = f"{self.subnet.rsplit('.', 1)[0]}.{self.cpu_base_ip + i}"
            nodes.append(NodeConfig(
                sysname=f"cpu{i+1}",
                role="cpu-server",
                ip=ip,
                auth=f"fs.{self.domain}",
                authdom=self.domain,
                fs=f"fs.{self.domain}",
                cpu=f"cpu{i+1}.{self.domain}",
                ports=[564, 17010, 17013],
            ))
        return nodes

    @property
    def all_nodes(self) -> list[NodeConfig]:
        return [self.fs_node] + self.cpu_nodes


