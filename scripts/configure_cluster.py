def init_cluster(project_path: str, cfg: ClusterConfig):
    """Generate all cluster configuration files."""
    p = Path(project_path)
    p.mkdir(parents=True, exist_ok=True)

    # Templates directory
    tpl = p / "templates"
    tpl.mkdir(exist_ok=True)

    # ndb/local
    ndb_file = tpl / "ndb-local.conf"
    ndb_file.write_text(gen_ndb_local(cfg))
    print(f"  + {ndb_file}")

    # File server cpurc
    cfg_dir = tpl / "cfg"
    cfg_dir.mkdir(exist_ok=True)
    fs_cpurc = cfg_dir / "fs-cpurc.rc"
    fs_cpurc.write_text(gen_cpurc_fs(cfg))
    print(f"  + {fs_cpurc}")

    # File server plan9.ini
    fs_ini = cfg_dir / "fs-plan9.ini"
    fs_ini.write_text(gen_plan9ini_fs(cfg))
    print(f"  + {fs_ini}")

    # CPU server configs
    for node in cfg.cpu_nodes:
        cpu_cpurc = cfg_dir / f"{node.sysname}-cpurc.rc"
        cpu_cpurc.write_text(gen_cpurc_cpu(cfg, node))
        print(f"  + {cpu_cpurc}")

        cpu_ini = cfg_dir / f"{node.sysname}-plan9.ini"
        cpu_ini.write_text(gen_plan9ini_cpu(cfg, node))
        print(f"  + {cpu_ini}")

    # Docker compose
    dc_file = p / "docker-compose.fileserver.yml"
    dc_file.write_text(gen_docker_compose(cfg))
    print(f"  + {dc_file}")

    # Cognitive scripts
    if cfg.cognitive_ns:
        scripts_dir = tpl / "scripts"
        scripts_dir.mkdir(exist_ok=True)

        cog_export = scripts_dir / "cognitive-export.rc"
        cog_export.write_text(gen_cognitive_exports_rc(cfg))
        print(f"  + {cog_export}")

        union_as = scripts_dir / "union-atomspace.rc"
        union_as.write_text(gen_union_atomspace_rc(cfg))
        print(f"  + {union_as}")

    # Cluster config JSON
    config_file = p / "cluster.json"
    config_file.write_text(json.dumps(asdict(cfg), indent=2) + "\n")
    print(f"  + {config_file}")

    print(f"\n  Cluster configured: 1 file server + {cfg.num_cpus} CPU servers")
    print(f"  Domain: {cfg.domain}  Subnet: {cfg.subnet}/{cfg.netmask}")


