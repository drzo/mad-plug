CLUSTER_PROMISES = [
    ("file-server",      "File server node configured (cpurc, plan9.ini, ndb)"),
    ("auth-server",      "Auth services enabled (keyfs, faktotum, tcp567)"),
    ("ndb-config",       "Network database has all nodes defined"),
    ("cpu-mount",        "CPU servers reference file server for mount"),
    ("cognitive-export", "Cognitive namespace exported via 9P2000"),
    ("docker-compose",   "docker-compose.fileserver.yml defines plan9-fileserver"),
    ("fs-storage",       "File system type configured (hjfs or cwfs)"),
    ("cluster-network",  "Cluster network subnet and gateway defined"),
]


def validate_cluster(project_path: str) -> list[dict]:
    """Validate a cluster configuration against file-server promises."""
    p = Path(project_path)
    results = []

    for name, desc in CLUSTER_PROMISES:
        satisfied = False
        evidence = ""

        if name == "file-server":
            cpurc = p / "cfg" / "fs" / "cpurc"
            if cpurc.exists():
                satisfied = True
                evidence = "cfg/fs/cpurc exists"
            elif (p / "templates" / "cfg" / "fs-cpurc.rc").exists():
                satisfied = True
                evidence = "templates/cfg/fs-cpurc.rc exists"

        elif name == "auth-server":
            for f in [p / "cfg" / "fs" / "cpurc", p / "templates" / "cfg" / "fs-cpurc.rc"]:
                if f.exists() and "keyfs" in f.read_text():
                    satisfied = True
                    evidence = f"auth/keyfs in {f.name}"
                    break

        elif name == "ndb-config":
            for f in [p / "lib" / "ndb" / "local", p / "templates" / "ndb-local.conf"]:
                if f.exists():
                    content = f.read_text()
                    if "sys=fs" in content and "sys=cpu" in content:
                        satisfied = True
                        evidence = f"File server and CPU nodes in {f.name}"
                        break

        elif name == "cpu-mount":
            for f in p.rglob("*cpurc*"):
                if f.is_file() and "mount" in f.read_text() and "fs" in f.read_text():
                    satisfied = True
                    evidence = f"File server mount in {f.name}"
                    break

        elif name == "cognitive-export":
            for f in p.rglob("*.rc"):
                if f.is_file() and "cognitive" in f.read_text() and "exportfs" in f.read_text():
                    satisfied = True
                    evidence = f"Cognitive export in {f.name}"
                    break
            if not satisfied:
                for f in p.rglob("*.yml"):
                    if f.is_file() and "COGNITIVE" in f.read_text().upper():
                        satisfied = True
                        evidence = f"Cognitive config in {f.name}"
                        break

        elif name == "docker-compose":
            dc = p / "docker-compose.fileserver.yml"
            if dc.exists() and "plan9-fileserver" in dc.read_text():
                satisfied = True
                evidence = "plan9-fileserver service in docker-compose.fileserver.yml"

        elif name == "fs-storage":
            for f in p.rglob("*"):
                if f.is_file() and f.suffix in [".rc", ".conf", ".yml", ".ini"]:
                    try:
                        content = f.read_text()
                        if "hjfs" in content or "cwfs" in content or "fossil" in content:
                            satisfied = True
                            evidence = f"File system type in {f.name}"
                            break
                    except Exception:
                        pass

        elif name == "cluster-network":
            for f in p.rglob("*"):
                if f.is_file() and f.suffix in [".conf", ""]:
                    try:
                        content = f.read_text()
                        if "ipnet=" in content or "subnet:" in content:
                            satisfied = True
                            evidence = f"Network config in {f.name}"
                            break
                    except Exception:
                        pass

        results.append({
            "name": name,
            "description": desc,
            "satisfied": satisfied,
            "evidence": evidence,
        })

    return results


